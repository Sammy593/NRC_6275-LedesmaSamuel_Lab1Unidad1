print("Ingresar dos numeros y realizar todas las operaciones aritmeticas")
#ingreso de datos
n1 = int(input('Primer numero:  '))
n2 = int(input('Segundo numero: '))

#operaciones
suma = str(n1+n2)
resta = str(n1-n2)
multiplicacion = str(n1*n2)
division = str(n1/n2)
potencia = str(n1**n2)

#mostrar resultados
print('Suma: ' + suma)
print('Resta: ' +  resta)
print('Mutiplicacion: ' + multiplicacion)
print('Division: ' + division)
print('Potencia: ' + potencia)